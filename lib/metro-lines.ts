export type MetroCardData = {
  station: string
  prev: string | null
  next: string | null
  color: string
  lineName: string
}

export function getMetroCardData(stationName: string): MetroCardData | null {
  for (const line of MOSCOW_LINES) {
    const idx = line.stations.indexOf(stationName)
    if (idx === -1) continue
    return {
      station: stationName,
      prev: idx > 0 ? line.stations[idx - 1] : null,
      next: idx < line.stations.length - 1 ? line.stations[idx + 1] : null,
      color: line.color,
      lineName: line.name,
    }
  }
  return null
}
